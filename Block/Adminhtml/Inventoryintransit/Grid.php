<?php
namespace Bsitc\Brightpearl\Block\Adminhtml\Inventoryintransit;

class Grid extends \Magento\Backend\Block\Widget\Grid\Extended
{
    /**
     * @var \Magento\Framework\Module\Manager
     */
    protected $moduleManager;

    /**
     * @var \Bsitc\Brightpearl\Model\inventoryintransitFactory
     */
    protected $_inventoryintransitFactory;

    /**
     * @var \Bsitc\Brightpearl\Model\Status
     */
    protected $_status;

    /**
     * @param \Magento\Backend\Block\Template\Context $context
     * @param \Magento\Backend\Helper\Data $backendHelper
     * @param \Bsitc\Brightpearl\Model\inventoryintransitFactory $inventoryintransitFactory
     * @param \Bsitc\Brightpearl\Model\Status $status
     * @param \Magento\Framework\Module\Manager $moduleManager
     * @param array $data
     *
     * @SuppressWarnings(PHPMD.ExcessiveParameterList)
     */
    public function __construct(
        \Magento\Backend\Block\Template\Context $context,
        \Magento\Backend\Helper\Data $backendHelper,
        \Bsitc\Brightpearl\Model\InventoryintransitFactory $InventoryintransitFactory,
        \Bsitc\Brightpearl\Model\Status $status,
        \Magento\Framework\Module\Manager $moduleManager,
        array $data = []
    ) {
        $this->_inventoryintransitFactory = $InventoryintransitFactory;
        $this->_status = $status;
        $this->moduleManager = $moduleManager;
        parent::__construct($context, $backendHelper, $data);
    }

    /**
     * @return void
     */
    protected function _construct()
    {
        parent::_construct();
        $this->setId('postGrid');
        $this->setDefaultSort('id');
        $this->setDefaultDir('DESC');
        $this->setSaveParametersInSession(true);
        $this->setUseAjax(false);
        $this->setVarNameFilter('post_filter');
    }

    /**
     * @return $this
     */
    protected function _prepareCollection()
    {
        $collection = $this->_inventoryintransitFactory->create()->getCollection();
        $this->setCollection($collection);

        parent::_prepareCollection();

        return $this;
    }

    /**
     * @return $this
     * @SuppressWarnings(PHPMD.ExcessiveMethodLength)
     */
    protected function _prepareColumns()
    {
        $this->addColumn(
            'id',
            [
                'header' => __('ID'),
                'type' => 'number',
                'index' => 'id',
                'header_css_class' => 'col-id',
                'column_css_class' => 'col-id'
            ]
        );


        
                $this->addColumn(
                    'source_warehouse_id',
                    [
                        'header' => __('Source Warehouse'),
                        'index' => 'source_warehouse_id',
                    ]
                );
                
                $this->addColumn(
                    'target_warehouse_id',
                    [
                        'header' => __('Target Warehouse'),
                        'index' => 'target_warehouse_id',
                    ]
                );
                
                $this->addColumn(
                    'stock_transfer_id',
                    [
                        'header' => __('Stock Transfer Id'),
                        'index' => 'stock_transfer_id',
                    ]
                );
                
                $this->addColumn(
                    'product_id',
                    [
                        'header' => __('Product Id'),
                        'index' => 'product_id',
                    ]
                );
                
                $this->addColumn(
                    'quantity',
                    [
                        'header' => __('Quantity'),
                        'index' => 'quantity',
                    ]
                );
                
                $this->addColumn(
                    'transfer',
                    [
                        'header' => __('Transfer Status'),
                        'index' => 'transfer',
                    ]
                );
                
                
                $this->addColumn(
                    'status',
                    [
                        'header' => __('Transfer Status'),
                        'index' => 'status',
                    ]
                );
                
                $this->addColumn(
                    'created_at',
                    [
                        'header' => __('Create At'),
                        'index' => 'created_at',
                        'type'      => 'datetime',
                    ]
                );
                    
                $this->addColumn(
                    'updated_at',
                    [
                        'header' => __('Updated On'),
                        'index' => 'updated_at',
                        'type'      => 'datetime',
                    ]
                );
                



        
        //$this->addColumn(
            //'edit',
            //[
                //'header' => __('Edit'),
                //'type' => 'action',
                //'getter' => 'getId',
                //'actions' => [
                    //[
                        //'caption' => __('Edit'),
                        //'url' => [
                            //'base' => '*/*/edit'
                        //],
                        //'field' => 'id'
                    //]
                //],
                //'filter' => false,
                //'sortable' => false,
                //'index' => 'stores',
                //'header_css_class' => 'col-action',
                //'column_css_class' => 'col-action'
            //]
        //);
        

        
           $this->addExportType($this->getUrl('brightpearl/*/exportCsv', ['_current' => true]), __('CSV'));
           $this->addExportType($this->getUrl('brightpearl/*/exportExcel', ['_current' => true]), __('Excel XML'));

        $block = $this->getLayout()->getBlock('grid.bottom.links');
        if ($block) {
            $this->setChild('grid.bottom.links', $block);
        }

        return parent::_prepareColumns();
    }

    
    /**
     * @return $this
     */
    protected function _prepareMassaction()
    {

        $this->setMassactionIdField('id');
        //$this->getMassactionBlock()->setTemplate('Bsitc_Brightpearl::inventoryintransit/grid/massaction_extended.phtml');
        $this->getMassactionBlock()->setFormFieldName('inventoryintransit');

        $this->getMassactionBlock()->addItem(
            'delete',
            [
                'label' => __('Delete'),
                'url' => $this->getUrl('brightpearl/*/massDelete'),
                'confirm' => __('Are you sure?')
            ]
        );

        $statuses = $this->_status->getOptionArray();

        $this->getMassactionBlock()->addItem(
            'status',
            [
                'label' => __('Change status'),
                'url' => $this->getUrl('brightpearl/*/massStatus', ['_current' => true]),
                'additional' => [
                    'visibility' => [
                        'name' => 'status',
                        'type' => 'select',
                        'class' => 'required-entry',
                        'label' => __('Status'),
                        'values' => $statuses
                    ]
                ]
            ]
        );


        return $this;
    }
        

    /**
     * @return string
     */
    public function getGridUrl()
    {
        return $this->getUrl('brightpearl/*/index', ['_current' => true]);
    }

    /**
     * @param \Bsitc\Brightpearl\Model\inventoryintransit|\Magento\Framework\Object $row
     * @return string
     */
    public function getRowUrl($row)
    {
        
        return $this->getUrl(
            'brightpearl/*/edit',
            ['id' => $row->getId()]
        );
    }
}
